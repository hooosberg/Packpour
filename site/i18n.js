function pickInitialLocale() {
  try {
    const stored = localStorage.getItem(STORAGE_KEY);
    if (stored && LOCALES[stored]) return stored;
  } catch (_) { /* private mode */ }

  const navLang = (navigator.language || "en").toLowerCase();
  if (navLang.startsWith("zh")) {
    if (navLang.includes("tw") || navLang.includes("hk") || navLang.includes("mo") || navLang.includes("hant")) return "zh-TW";
    return "zh-CN";
  }
  const two = navLang.slice(0, 2);
  if (LOCALES[two]) return two;
  return "en";
}

function captureEnglishSources() {
  const map = new Map();
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    map.set(node, { mode: "text", value: node.textContent });
  });
  document.querySelectorAll("[data-i18n-html]").forEach((node) => {
    map.set(node, { mode: "html", value: node.innerHTML });
  });
  return map;
}

function applyLocale(locale, englishSources) {
  const dir = LOCALES[locale]?.dir || "ltr";
  document.documentElement.setAttribute("lang", locale === "en" ? "en" : locale);
  document.documentElement.setAttribute("dir", dir);

  const dict = STRINGS[locale];

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.getAttribute("data-i18n");
    if (locale === "en" || !dict || !(key in dict)) {
      const src = englishSources.get(node);
      if (src) node.textContent = src.value;
    } else {
      node.textContent = dict[key];
    }
  });

  document.querySelectorAll("[data-i18n-html]").forEach((node) => {
    const key = node.getAttribute("data-i18n-html");
    if (locale === "en" || !dict || !(key in dict)) {
      const src = englishSources.get(node);
      if (src) node.innerHTML = src.value;
    } else {
      node.innerHTML = dict[key];
    }
  });

  const label = document.getElementById("langSwitcherLabel");
  if (label) label.textContent = LOCALES[locale].name;
}

function buildLanguageMenu(current, onPick) {
  const menu = document.getElementById("langSwitcherMenu");
  if (!menu) return;
  menu.innerHTML = "";
  LOCALE_ORDER.forEach((code) => {
    const li = document.createElement("li");
    li.setAttribute("role", "menuitem");
    li.dataset.locale = code;
    li.textContent = LOCALES[code].name;
    if (code === current) li.classList.add("is-active");
    li.addEventListener("click", () => onPick(code));
    menu.appendChild(li);
  });
}

function initLangSwitcher(englishSources) {
  const btn = document.getElementById("langSwitcherButton");
  const menu = document.getElementById("langSwitcherMenu");
  if (!btn || !menu) return;

  let current = pickInitialLocale();
  applyLocale(current, englishSources);
  buildLanguageMenu(current, onPick);

  function setOpen(open) {
    btn.setAttribute("aria-expanded", String(open));
    menu.hidden = !open;
  }

  btn.addEventListener("click", (e) => {
    e.stopPropagation();
    setOpen(menu.hidden);
  });

  document.addEventListener("click", (e) => {
    if (!menu.contains(e.target) && e.target !== btn) setOpen(false);
  });

  document.addEventListener("keydown", (e) => {
    if (e.key === "Escape") setOpen(false);
  });

  function onPick(code) {
    current = code;
    try { localStorage.setItem(STORAGE_KEY, code); } catch (_) { /* ignore */ }
    applyLocale(code, englishSources);
    buildLanguageMenu(code, onPick);
    setOpen(false);
  }
}

function init() {
  const englishSources = captureEnglishSources();
  initLangSwitcher(englishSources);
  const yearEl = document.getElementById("year");
  if (yearEl) yearEl.textContent = String(new Date().getFullYear());
}

if (document.readyState === "loading") {
  document.addEventListener("DOMContentLoaded", init);
} else {
  init();
}
